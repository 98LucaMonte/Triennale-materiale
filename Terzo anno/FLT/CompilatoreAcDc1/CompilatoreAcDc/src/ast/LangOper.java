package ast;

/**
 * Classe di enumerativi che rappresentano le possibili operazioni eseguibili nel compilatore.
 * @author Luca Monteleone
 * */
public enum LangOper {
	/**
	 *enumerativo che rappresenta l'operatore somma
	*/
	PLUS,
	/**
	 *enumerativo che rappresenta l'operatore differenza
	*/
	MINUS,
	/**
	 *enumerativo che rappresenta l'operatore divisore
	*/
	DIV,
	/**
	 *enumerativo che rappresenta l'operatore prodotto
	*/
	TIMES
}
